#define OUI_TABLE_SIZE (sizeof(oui_table) / sizeof(oui_table[0]))

const char* oui_lookup(const uint8_t mac[3]) {
    for(uint16_t i = 0; i < OUI_TABLE_SIZE; i++) {
        if(mac[0] == oui_table[i].oui[0] && mac[1] == oui_table[i].oui[1] &&
           mac[2] == oui_table[i].oui[2]) {
            return oui_table[i].vendor;
        }
    }
    return "Unknown";
}
